txi_handlers = {
    param = {
        [1] = function(val)
            val = val / 10
            val = val * 8
            val = math.floor(val + 0.5)
            val = -48 + 12 * val
            print(val)
            output[1].volts = note_map[val]
        end,
    },
    ['in'] = { -- in is a lua keyword
        [1] = function(val)
        end,
    }
}

ii.txi.event = function(e, val)
    local handler = txi_handlers[e.name][e.arg]
    if handler then
        handler(val)
    end
end

txi_metro = metro.init{
    time  = 0.01,
    count = -1,
    event = function()
        ii.txi.get('param', 1)
        ii.txi.get('in', 1)
    end,
}
